export function getHdcKeyCode(keyCode1: string, keyCode2?: string): number | null {
  if (keyCode1) {
    const upper = keyCode1.toUpperCase();
    if (KEY_CODE_MAP[upper] !== undefined) return KEY_CODE_MAP[upper];
  }
  if (keyCode2) {
    const upper = keyCode2.toUpperCase();
    if (KEY_CODE_MAP[upper] !== undefined) return KEY_CODE_MAP[upper];
  }
  // 模糊匹配
  for (const [key, code] of Object.entries(KEY_CODE_MAP)) {
    const stripped = key.replace(/_/g, '').toLowerCase();
    if (stripped.includes((keyCode1 || '').toLowerCase()) ||
        stripped.includes((keyCode2 || '').toLowerCase())) {
      return code;
    }
  }
  return null;
}
